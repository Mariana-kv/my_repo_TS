console.log('1. Loop with WHILE');
let i = 0;
while (i < 9) {
    console.log(i);
    i++;
};

console.log('2. Loop with FOR');
let j;
for (j = 0; j < 9; j++) {
    console.log(j);
};

console.log('3. Loop with DO WHILE');
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 9);

console.log('4. Loop with WHILE');
let m = 100;
while (m > 0) {
    console.log(m);
    m = m - 10;
};

console.log('5. Loop with FOR');
let d;
for (d = 100; d > 0; d -= 10) {
    console.log(d);
};

console.log('6. Loop with DO WHILE');
let s = 100;
do {
    console.log(s);
    s -= 10;
} while (s > 0);
